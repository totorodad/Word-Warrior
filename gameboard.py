def word_warrior_score(word):
    total = 0
    for letter in word.lower():
        total += letter_value[letter]
    return total

#Calculate how many tile left in the bag
def how_many_tiles_left():
    return (len(word_warrior_bag))

#Insert a at a random location in the array a character
def insert_random(arr, char):
  index = random.randint(0, len(arr))
  arr.insert(index, char)
  return arr

#Take all the letters in the word_warrior key and mix them up into the word_warrior bag
def mix_word_warrior_bag ():
    for key in word_warrior_letter_key:
        for i in range (0,word_warrior_letter_key[key]):
            insert_random(word_warrior_bag,key)

#If there are empty spots and tiles available in the bag then refill the identified players hand
def refill_hand (player):
    for i in range (0,7):
        if (player[i] == '#' and len(word_warrior_bag)>0):
            player[i] = word_warrior_bag.pop()

